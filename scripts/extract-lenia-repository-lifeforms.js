const fs = require("fs");
const os = require("os");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const LENIA_ROOT_CANDIDATES = [
  process.env.LENIA_REPOSITORY,
  path.resolve(ROOT, "..", "Lenia"),
  path.join(os.homedir(), "Documents", "Lenia"),
].filter(Boolean);

const LENIA_ROOT = LENIA_ROOT_CANDIDATES.find((candidate) =>
  fs.existsSync(path.join(candidate, "Python", "animals.json")),
);
if (!LENIA_ROOT) {
  throw new Error(
    "Unable to find Lenia Python/animals.json. Set LENIA_REPOSITORY to a local Lenia checkout.",
  );
}
const SOURCE_FILE = path.join(LENIA_ROOT, "Python", "animals.json");
const OUT_FILE = path.join(ROOT, "src", "lenia-repository-lifeforms.js");

function formatNumber(value) {
  const number = Number(value);
  if (!Number.isFinite(number)) return String(value ?? 0);
  return Number(number.toPrecision(12)).toString();
}

function kernelName(params) {
  switch (Number(params.kn || 1)) {
    case 2:
      return "quad4";
    case 3:
      return "stpz1/4";
    case 4:
      return "life";
    default:
      return "bump4";
  }
}

function growthName(params) {
  switch (Number(params.gn || 1)) {
    case 2:
      return "quad4";
    case 3:
      return "stpz";
    default:
      return "gaus";
  }
}

function kernelExpression(params) {
  const name = kernelName(params);
  const beta = String(params.b || "1");
  if (name === "life") return "life(1)";
  return beta && beta !== "1" ? `${name}(${beta})` : name;
}

function paramsToRule(params, cells) {
  const source = Array.isArray(params) ? params[0] : params || {};
  const timestep = Number(source.T || 10);
  const dt = timestep > 0 ? 1 / timestep : 0.1;
  return [
    `R=${formatNumber(source.R || 13)}`,
    `k=${kernelExpression(source)}`,
    `d=${growthName(source)}(${formatNumber(source.m ?? 0.15)},${formatNumber(source.s ?? 0.015)})*${formatNumber(dt)}`,
    `cells=${cells}`,
  ].join(";");
}

function normalizeEntry(entry) {
  const code = String(entry.code || "");
  if (code.startsWith(">")) return [code, String(entry.name || ""), String(entry.cname || "")];
  if (!entry.cells || !entry.params || (!entry.code && !entry.name)) return null;
  const cells = Array.isArray(entry.cells) ? entry.cells[0] : String(entry.cells);
  return [code, String(entry.name || code), String(entry.cname || ""), paramsToRule(entry.params, cells)];
}

function escapeNonAscii(text) {
  return text.replace(/[^\x00-\x7f]/g, (char) => {
    return `\\u${char.charCodeAt(0).toString(16).padStart(4, "0")}`;
  });
}

function main() {
  const source = JSON.parse(fs.readFileSync(SOURCE_FILE, "utf8"));
  const entries = source.map(normalizeEntry).filter(Boolean);
  const lifeformCount = entries.filter((entry) => entry.length >= 4).length;
  const body = JSON.stringify(entries, null, 2);
  const output = [
    "// Generated by scripts/extract-lenia-repository-lifeforms.js.",
    "// Source: LENIA_REPOSITORY/Python/animals.json",
    "window.leniaRepositoryAnimalArr = ",
    `${escapeNonAscii(body)};`,
    "",
  ].join("\n");

  fs.writeFileSync(OUT_FILE, output, "utf8");
  console.log(`Extracted ${lifeformCount} Lenia repository lifeforms to ${path.relative(ROOT, OUT_FILE)}`);
}

main();
